#include <iostream>
using namespace std;

int main()
{
    int c[5][3][2] = {
        {{111, 112}, {121, 122}, {131, 132}}, 
        {{211, 212}, {221, 222}, {231, 232}}, 
        {{311, 312}, {321, 322}, {331, 332}}, 
        {{411, 412}, {421, 422}, {431, 432}}, 
        {{511, 512}, {521, 522}, {531, 532}}
        };
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 3; j++) {
            for (int k = 0; k < 2; k++) {
                cout << "c[" << i << "][" << j << "][" << k << "]: " << c[i][j][k] << endl;
            }
        }
    }
}